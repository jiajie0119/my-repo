    private void uploadTxtButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadTxtButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File("C:\\BCS3232_NetBeans\\Assignment"));
        int returnValue = fileChooser.showOpenDialog(this);
        Decryptor decrypt = new Decryptor();

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            decrypt.readTxtFile(selectedFile);
            cipherField.setText(selectedFile.toString());
        }
        
    }//GEN-LAST:event_uploadTxtButtonActionPerformed

    private void encrypButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encrypButtonActionPerformed
        String plainText = textToEncrypt.getText();  
        String vigenereKey = vigenereKeyToShift.getText();
        String vernamKey = vernamKeyToShift.getText();
        String result = "";
        
        try{
            // Delay for 2 seconds (not recommended practice, usually used for demonstration purposes)
            Thread.sleep(2000);

            // Encrypt the file using DESEncrypt class (assuming DESEncrypt is defined elsewhere)
            result = Encryptor.combineCiphers(plainText,vigenereKey,vernamKey);
            
            } catch (Exception e) {
            // Catch any exceptions that occur during file selection or encryption
            System.out.println("Exception in EncryptTXT");
        }
            
        PopUpSuccess pus = new PopUpSuccess();
        pus.setVisible(true);
        cipherTextField.setText(result);
    }//GEN-LAST:event_encrypButtonActionPerformed


    private void uploadKeyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadKeyButtonActionPerformed
        Decryptor decrypt = new Decryptor();
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File("C:\\BCS3232_NetBeans\\Assignment"));
        int returnValue = fileChooser.showOpenDialog(this);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            decrypt.readKeyFile(selectedFile);
            keyField.setText(selectedFile.toString());
        }
    }//GEN-LAST:event_uploadKeyButtonActionPerformed

    private void decryptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_decryptButtonActionPerformed
        Decryptor decrypt = new Decryptor();
        String plainTxt = decrypt.decryptMessage();
        plainTextField.setText(plainTxt.toString());
    }//GEN-LAST:event_decryptButtonActionPerformed

